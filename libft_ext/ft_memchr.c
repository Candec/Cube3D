#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ch;

	i = 0;
	ch = (unsigned char *)s;
	while (i < n)
	{
		if (ch[i] == (unsigned char)c)
			return (ch += i);
		i++;
	}
	return (0);
}
